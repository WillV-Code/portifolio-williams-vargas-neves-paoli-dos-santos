@auditor
def calcular_orcamento(estrutura, *departamentos_ignorados, **kwargs): # ← Função principal para o cálculo (recursiva)

    taxa_cambio = kwargs.get("taxa_cambio", 1)
    moeda_destino = kwargs.get("moeda_destino", "ORIGINAL")

    def dicionario_percorrer(num):
      total = 0

      for nome, valor in num.items():
          # Ignora o departamento inteiro (e seus subníveis)
          if nome in departamentos_ignorados:
            continue

          # Se for um subdepartamento, continua a recursão
          if isinstance(valor, dict):
            total += dicionario_percorrer(valor)

          # Se for um valor númerico, soma diretamente
          elif isinstance(valor, (int, float)):
            total += valor

      return total

    total_base = dicionario_percorrer(estrutura)
    orcamento_total = total_base * taxa_cambio

    print(f"Moeda de destino: {moeda_destino}")
    print(f"Taxa de câmbio: {taxa_cambio}")

    return orcamento_total

