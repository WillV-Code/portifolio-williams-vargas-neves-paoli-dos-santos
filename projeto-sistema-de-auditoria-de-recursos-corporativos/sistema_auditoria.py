# Importar Biblioteca time para tempo e wraps pra não perder o valor original
import time
from functools import wraps

# Decorator da auditoria
def auditor(func):
  @wraps(func) # ← Chamando decorator na função
  def wrapper(*args, **kwargs):
    print("\n--- Ínicio da Auditoria ---")
    print(f"Função chamada: {func.__name__}")
    print(f"Args (posicionais): {args}")
    print(f"Kwargs (nomeados): {kwargs}")

    inicio = time.time()
    resultado = func(*args, **kwargs)
    fim = time.time()

    print(f"Tempo de execução: {fim - inicio:.6f} segundos")
    print("--- Fim da auditoria ---")
    return resultado
  return wrapper

